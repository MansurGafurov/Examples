void PrintArr (int[,] arr)
{
    for(int x = 0; x < arr.GetLength(0); x++)
    {
        for(int y = 0; y < arr.GetLength(1); y++)
        {
            if(arr[x, y] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FullArr (int x , int y)
{
    if(pic[x,y] == 0)
    {
        pic[x,y] = 1;
        FullArr(x-1, y);
        FullArr(x, y-1);
        FullArr(x+1, y);
        FullArr(x, y+1);
    }
}


